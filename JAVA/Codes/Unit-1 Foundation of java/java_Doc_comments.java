/*
    Java Doc Comments:
    ===================
        Java doc comments are used to create documentation for Java code. 
        The javadoc tool parses the Java source code and generates HTML documentation.
        
        Syntax:
            /**
             * Description of the class
             * @author author_name
             * @version version_number
             * @see reference
             * @since release
             * @param parameter_name description
             * @return description
             * @exception exception_name description
             * @deprecated description
             * /
        
        Example:
            /**
             * This class represents a car.
             * @author John Doe
             * @version 1.0
             * @since 2021
             * /
            public class Car {
                String model;
                int year;
                
                /**
                 * This method drives the car.
                 * @param speed The speed of the car
                 * @return void
                 * /
                void drive(int speed) {
                    System.out.println("Driving at speed: " + speed);
                }
            }
 */