/*

Overview of OOP
----------------



    Object-Oriented Programming Paradigms
    -------------------------------------

        Object-Oriented Programming (OOP) is a programming paradigm based on the concept of "objects," which can contain data and methods. It includes four main principles: Encapsulation, Inheritance, Polymorphism, and Abstraction.

        object:
        --------
            An object is an instance of a class that contains data and methods. It represents a real-world entity or concept.

        class:
        -------
            A class is a blueprint for creating objects. It defines the properties and methods that an object will have.

        Encapsulation:
        --------------
            Encapsulation is the bundling of data and methods that operate on that data into a single unit or class. 
            It helps in hiding the internal state of an object and only exposing the necessary functionality.

        Inheritance:
        ------------
            Inheritance allows a class to inherit properties and methods from another class. 
            It promotes code reuse and allows for the creation of a hierarchy of classes.

        Polymorphism:
        -------------
            Polymorphism allows objects of different classes to be treated as objects of a common superclass. 
            It enables the use of a single interface to represent different data types or objects.

        Abstraction:
        ------------
            Abstraction is the process of hiding the implementation details and showing only the essential features of an object. 
            It helps in managing complexity by providing a simplified view of the object.




*/
